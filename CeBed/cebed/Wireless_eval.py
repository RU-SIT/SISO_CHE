import os
import numpy as np
import tensorflow as tf
import pandas as pd
import matplotlib.pyplot as plt

# Import CeBed Trainer and TrainConfig
from cebed.trainer import Trainer, TrainConfig

# Custom model wrapper to return saved predictions
class SavedPredictionsModel(tf.keras.Model):
    def __init__(self, predictions):
        super(SavedPredictionsModel, self).__init__()
        self.predictions = predictions
        self.pointer = 0  # pointer to iterate over predictions

    def call(self, inputs, training=False):
        batch_size = tf.shape(inputs)[0]
        preds = self.predictions[self.pointer : self.pointer + batch_size]
        self.pointer += batch_size
        if self.pointer >= self.predictions.shape[0]:
            self.pointer = 0
        return preds

# Function to evaluate a given set of predictions using CeBed/Sionna logic
def evaluate_predictions(prediction_file, snr_range, experiment_name):
    # Load your saved predictions (shape: [N_samples, height, width, channels])
    predictions = np.load(prediction_file)

    # Configure CeBed trainer (assuming dataset/environment is correctly set up)
    config = TrainConfig(
        experiment_name=experiment_name,
        data_dir="./datasets",
        dataset_name="SionnaOfflineMD",
        model_name="CustomModel",
        seed=42,
        verbose=1,
        output_dir="train_output",
        epochs=1,
        train_batch_size=32,
        eval_batch_size=32,
        lr=0.001,
        loss_fn="mse",
        train_split=0.9,
        early_stopping=False,
        input_type="low"
    )

    trainer = Trainer(config)
    trainer.setup()

    # Replace trainer's model with your custom prediction model
    trainer.model = SavedPredictionsModel(predictions)

    # Evaluate the model across specified SNR range (also evaluates LS, LMMSE baselines)
    trainer.evaluate(snr_range, baselines=["LS", "LMMSE"], save=True)

    # Load results from CSV generated by CeBed evaluation method
    mse_csv_path = os.path.join(trainer.log_dir, "Meta_test_mses.csv")
    mse_df = pd.read_csv(mse_csv_path)

    return mse_df

# Define your prediction files (update these paths accordingly)
maml_predictions_file = "path/to/MAML_predictions.npy"
imaml_predictions_file = "path/to/iMAML_predictions.npy"

# Define your desired SNR range for evaluation
snr_range = [-5, 0, 5, 10, 15, 20, 25, 30]

# Evaluate MAML and iMAML models separately
maml_mse_df = evaluate_predictions(maml_predictions_file, snr_range, "MAML_eval")
imaml_mse_df = evaluate_predictions(imaml_predictions_file, snr_range, "iMAML_eval")

# Combine results for plotting
maml_mse_df["Model"] = "MAML"
imaml_mse_df["Model"] = "iMAML"

combined_df = pd.concat([maml_mse_df, imaml_mse_df], ignore_index=True)

# Plot MSE vs. SNR curves clearly comparing both models
plt.figure(figsize=(10, 6))
for model in combined_df["Model"].unique():
    subset = combined_df[combined_df["Model"] == model]
    plt.plot(subset["snr"], subset["mse"], marker='o', label=model)

# Optionally plot baseline methods (LS/LMMSE)
for baseline in ["LS", "LMMSE"]:
    baseline_subset = maml_mse_df[maml_mse_df["method"] == baseline]
    plt.plot(baseline_subset["snr"], baseline_subset["mse"], linestyle='--', marker='x', label=baseline)

plt.xlabel("SNR (dB)", fontsize=14)
plt.ylabel("Mean Squared Error (MSE)", fontsize=14)
plt.title("MSE vs. SNR Comparison: MAML vs. iMAML", fontsize=16)
plt.legend()
plt.grid(True)
plt.tight_layout()

# Save and show the plot clearly labeled with date/time for record keeping
output_plot_path = f"MSE_vs_SNR_MAML_iMAML_{pd.Timestamp.now().strftime('%Y%m%d_%H%M%S')}.png"
plt.savefig(output_plot_path, dpi=300)
plt.show()

print(f"✅ Plot saved successfully at: {output_plot_path}")
